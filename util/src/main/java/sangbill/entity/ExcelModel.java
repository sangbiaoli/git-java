package com.sangbill.entity;

import java.util.Date;

public class ExcelModel {
	private String estr;
	private Integer eint;
	private Date edate;
	private Date edateTime;
	public String getEstr() {
		return estr;
	}
	public void setEstr(String estr) {
		this.estr = estr;
	}
	public Integer getEint() {
		return eint;
	}
	public void setEint(Integer eint) {
		this.eint = eint;
	}
	public Date getEdate() {
		return edate;
	}
	public void setEdate(Date edate) {
		this.edate = edate;
	}
	public Date getEdateTime() {
		return edateTime;
	}
	public void setEdateTime(Date edateTime) {
		this.edateTime = edateTime;
	}
	
	
}
